using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ExerciseClassMethods
{
    public class MathOperations
    {
      public static int Square(int i)
        {
            int input = i;
            return input * input;
            
        }

       public static int MultbyTen(int i)
        {
           int input = i;
            return input * 10;
        }

       public static int AddbyTen(int i)
        {
            int input = i;
            return input + 10;
        }

        
    }

    
}
